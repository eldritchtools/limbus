export function getUptieCost(rank, start, target) {
    if (target <= start) return [0, 0];
    let thread = 0;
    let shard = 0;
    let cur = start;
    while (cur < target) {
        thread += uptie[rank][cur][0];
        shard += uptie[rank][cur][1];
        cur++;
    }
    return [thread, shard];
}

